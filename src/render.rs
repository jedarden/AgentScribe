#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_render_html_basic() {
        let events = vec![Event::new(
            Utc::now(),
            "test/123".to_string(),
            "test".to_string(),
            Role::User,
            "Hello, world!".to_string(),
        )];
        let meta = SessionManifest::new("test/123".to_string(), "test".to_string());

        let html = render_html(&events, &meta).unwrap();
        assert!(html.contains("<!DOCTYPE html>"));
        assert!(html.contains("Hello, world!"));
        assert!(html.contains("role-user"));
    }

    #[test]
    fn test_render_markdown_basic() {
        let events = vec![Event::new(
            Utc::now(),
            "test/123".to_string(),
            "test".to_string(),
            Role::User,
            "Hello, world!".to_string(),
        )];
        let meta = SessionManifest::new("test/123".to_string(), "test".to_string());

        let md = render_markdown(&events, &meta).unwrap();
        assert!(md.starts_with("---\n"));
        assert!(md.contains("**User**"));
        assert!(md.contains("Hello, world!"));
    }

    #[test]
    fn test_render_html_with_code_block() {
        let events = vec![Event::new(
            Utc::now(),
            "test/123".to_string(),
            "test".to_string(),
            Role::Assistant,
            "Here is some code:\n```rust\nfn main() {}\n```".to_string(),
        )];
        let meta = SessionManifest::new("test/123".to_string(), "test".to_string());

        let html = render_html(&events, &meta).unwrap();
        assert!(html.contains("<pre><code class=\"language-rust\">"));
        assert!(html.contains("fn main() {}"));
    }

    #[test]
    fn test_escape_html() {
        assert_eq!(escape_html("<script>"), "&lt;script&gt;");
        assert_eq!(escape_html("a & b"), "a &amp; b");
        assert_eq!(escape_html("\"quoted\""), "&quot;quoted&quot;");
    }

    #[test]
    fn test_format_duration() {
        let start = Utc::now();
        let end = start + chrono::Duration::hours(2) + chrono::Duration::minutes(30);
        assert_eq!(format_duration(start, Some(end)), "2h 30m");
    }
}
