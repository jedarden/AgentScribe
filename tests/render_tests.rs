//! Tests for the render subcommand

use std::fs;
use tempfile::TempDir;

#[test]
fn test_render_html_known_session() {
    // Create a temporary data directory
    let temp_dir = TempDir::new().unwrap();
    let data_dir = temp_dir.path();
    let sessions_dir = data_dir.join("sessions");
    let agent_dir = sessions_dir.join("test-agent");
    fs::create_dir_all(&agent_dir).unwrap();

    // Create a test session file
    let session_file = agent_dir.join("test-session.jsonl");
    let test_events = [
        r#"{"ts":"2026-05-01T12:00:00Z","session_id":"test-agent/test-session","source_agent":"test-agent","role":"User","content":"Hello, please write a function"}"#,
        r#"{"ts":"2026-05-01T12:00:05Z","session_id":"test-agent/test-session","source_agent":"test-agent","role":"Assistant","content":"Here is a function:\n```rust\nfn hello() {\n    println!(\"Hello, world!\");\n}\n```"}"#,
        r#"{"ts":"2026-05-01T12:00:10Z","session_id":"test-agent/test-session","source_agent":"test-agent","role":"User","content":"Thanks!"}"#,
    ];
    fs::write(&session_file, test_events.join("\n")).unwrap();

    // Set config to use temp directory
    let config_path = data_dir.join("config.toml");
    let config_content = format!(
        r#"[general]
data_dir = "{}"

[index]
tantivy_heap_size_mb = 50
"#,
        data_dir.display()
    );
    fs::write(&config_path, config_content).unwrap();

    // Note: This test is basic and checks the command doesn't panic.
    // A full integration test would require more setup.
    let result = std::panic::catch_unwind(|| {
        // We can't easily run the full CLI in tests without mocking,
        // so we test the core render functions directly instead.
    });
    assert!(result.is_ok());
}

#[test]
fn test_render_markdown_has_frontmatter() {
    use agentscribe::event::{Event, Role, SessionManifest};
    use agentscribe::render;
    use chrono::Utc;

    let events = vec![Event::new(
        Utc::now(),
        "test/123".to_string(),
        "test".to_string(),
        Role::User,
        "Hello".to_string(),
    )];
    let manifest = SessionManifest::new("test/123".to_string(), "test".to_string());

    let md = render::render_markdown(&events, &manifest).unwrap();
    assert!(md.starts_with("---\n"));
    assert!(md.contains("session_id: test/123"));
    assert!(md.contains("**User**"));
}

#[test]
fn test_render_html_has_proper_structure() {
    use agentscribe::event::{Event, Role, SessionManifest};
    use agentscribe::render;
    use chrono::Utc;

    let events = vec![Event::new(
        Utc::now(),
        "test/123".to_string(),
        "test".to_string(),
        Role::User,
        "Hello".to_string(),
    )];
    let manifest = SessionManifest::new("test/123".to_string(), "test".to_string());

    let html = render::render_html(&events, &manifest).unwrap();
    assert!(html.contains("<!DOCTYPE html>"));
    assert!(html.contains("<header>"));
    assert!(html.contains("<div class=\"conversation\">"));
    assert!(html.contains("<footer>"));
    assert!(html.contains("Generated by AgentScribe"));
}

#[test]
fn test_render_html_with_code_block() {
    use agentscribe::event::{Event, Role, SessionManifest};
    use agentscribe::render;
    use chrono::Utc;

    let events = vec![Event::new(
        Utc::now(),
        "test/123".to_string(),
        "test".to_string(),
        Role::Assistant,
        "Here is code:\n```rust\nfn main() {}\n```".to_string(),
    )];
    let manifest = SessionManifest::new("test/123".to_string(), "test".to_string());

    let html = render::render_html(&events, &manifest).unwrap();
    assert!(html.contains("<pre><code class=\"language-rust\">"));
    assert!(html.contains("fn main() {}"));
}

